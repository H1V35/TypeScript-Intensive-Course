// --------- TYPES OR INTERFACES ---------
// #######################################

// Interfaces cannot declare a primitive type whereas, types can

// In general, if we had to choose one, we would always try to use types
// because they are more versatile

// Types cannot be duplicated

// Interfaces are better identified when dealing with objects or classes

// Do you need to define a new object, or a method of an object, or the
// shape of a class? --> INTERFACES.
// The benefit is the merging of declarations

// For primitive type aliases, defining tuples, defining unions,
// overloading functions, ... --> TYPES
